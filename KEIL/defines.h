#define SETBIT(WORD,BITPOS) WORD|=(1<<BITPOS)
#define CLRBIT(WORD,BITPOS) WORD&=~(1<<BITPOS)
#define TOGBIT(WORD,BITPOS) WORD^=(1<<BITPOS)
#define WRITEBIT(WORD,BITPOS,BITLEVEL) WORD=((WORD&~(1<<BITPOS))|(BITLEVEL<<BITPOS))
#define WRITEBIT(WORD,BITPOS,BITLEVEL) WORD=((WORD&~(1<<BITPOS))|(BITLEVEL<<BITPOS))
